package com.example.alineservice.model;

public class TipoBoleta {
    private int idTipoBoleta;
    private String nombreTipoBoleta;

    public int getIdTipoBoleta() {
        return idTipoBoleta;
    }

    public void setIdTipoBoleta(int idTipoBoleta) {
        this.idTipoBoleta = idTipoBoleta;
    }

    public String getNombreTipoBoleta() {
        return nombreTipoBoleta;
    }

    public void setNombreTipoBoleta(String nombreTipoBoleta) {
        this.nombreTipoBoleta = nombreTipoBoleta;
    }
}
